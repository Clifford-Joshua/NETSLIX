const ListConatiner = document.querySelector(".movie_area");
const buttoncontainer = document.querySelector(".sub_nav");
// on load display all items
window.addEventListener("DOMContentLoaded", function () {
  DisplayAllMovies(List);
  ShowALLBtn();
  bg();
});
// function to display the listed items
function DisplayAllMovies(Listeditems) {
  const Allitems = Listeditems.map(function (eachitems) {
    return `     <article class="movie_desc">
            <!-- icon in place of an image -->
            <img src=${eachitems.img} alt=${eachitems.title}/>
            <div class="text">
              <h4 class="title">${eachitems.title}</h4>
              <div class="underline"></div>
              <!-- series -->
              <p class="author">${eachitems.author}</p>
              <!-- status -->
              <p class="status">${eachitems.status}......</p>
              <p class="red"><a href=${eachitems.link}>Watch / Read</a></p>
              <div class="showbtn">
                summary
                <details>
                ${eachitems.desc}
                </details>
              </div>
            </div>
          </article>`;
  }).join("");
  ListConatiner.innerHTML = Allitems;
}
// function to display the button not hard coded
function ShowALLBtn() {
  const displaybtn = List.reduce(
    function (value, items) {
      if (!value.includes(items.catergory)) {
        value.push(items.catergory);
      }
      return value;
    },
    ["All"]
  );
  const Displayallbtn = displaybtn
    .map(function (items) {
      return `<button class="filter-btn" data-id=${items} type="button">
            ${items}
          </button>`;
    })
    .join("");
  buttoncontainer.innerHTML = Displayallbtn;
  const filterbtn = document.querySelectorAll(".filter-btn");
  filterbtn.forEach(function (Eachfilterbtn) {
    Eachfilterbtn.addEventListener("click", (e) => {
      const currentBtnid = e.currentTarget.dataset.id;
      const filteredBtn = List.filter(function (FilteringBtn) {
        if (FilteringBtn.catergory === currentBtnid) {
          return FilteringBtn;
        }
      });
      if (currentBtnid == "All") {
        DisplayAllMovies(List);
      } else {
        DisplayAllMovies(filteredBtn);
      }
    });
  });
}
function bg() {
  const sectionbody = document.querySelector(".first_section");
  const innerbody = document.querySelector(".inner_body_section");
  const click = document.querySelectorAll(".click-btn");
  // let light = document.querySelector(".checks");
  click.forEach((eachbtn) => {
    eachbtn.addEventListener("click", (e) => {
      const clickbtn = e.currentTarget.classList;
      if (clickbtn.contains("click-btn")) {
        sectionbody.classList.toggle("move");
      }
      if (clickbtn.contains("home")) {
        DisplayAllMovies(List);
      }
      if (clickbtn.contains("btn")) {
        innerbody.classList.toggle("light");
        sectionbody.classList.toggle("show");
      }
    });
  });
}
